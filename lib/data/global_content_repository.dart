  static List<PhraseEntry> phrasesFor(
    String languageCode, {
    String sourceLanguageCode = 'en',
  }) {
    if (!coreLanguageCodes.contains(languageCode)) return const [];
    final code = languageCode;
    final sourceCode =
        coreLanguageCodes.contains(sourceLanguageCode) &&
            sourceLanguageCode != languageCode
        ? sourceLanguageCode
        : 'en';
    return concepts
        .map(
          (item) => PhraseEntry(
            source: item.translations[sourceCode] ?? item.source,
            target: item.translations[code] ?? item.source,
            category: item.category,
            note: 'LingoNexa global core',
            visual: visualFor(item.category, item.source),
          ),
        )
        .toList(growable: false);
  }

  static List<SentenceDrill> sentenceDrillsFor(
    String languageCode, {
    String sourceLanguageCode = 'en',
  }) {
    final phrases = phrasesFor(
      languageCode,
      sourceLanguageCode: sourceLanguageCode,
    );
    const missions = [
      'Recognize the meaning',
      'Recall without looking',
      'Say it with natural rhythm',
      'Use it in a real situation',
    ];
    return [
      for (final phrase in phrases)
        for (final mission in missions)
          SentenceDrill(
            source: phrase.source,
            target: phrase.target,
            category: phrase.category,
            mission: mission,
            visual: phrase.visual,
          ),
    ];
  }

  static int get localizedPhrasePairs =>
      concepts.length * coreLanguageCodes.length;
  static int get sentenceDrillCount => localizedPhrasePairs * 4;

  static TranslationResult? translateExact(
    String input, {
    required String sourceLanguageCode,
    required String targetLanguageCode,
  }) {
    if (!coreLanguageCodes.contains(sourceLanguageCode) ||
        !coreLanguageCodes.contains(targetLanguageCode)) {
      return null;
    }
    final normalized = _normalize(input);
    if (normalized.isEmpty) return null;
    for (final concept in concepts) {
      final source = concept.translations[sourceLanguageCode];
      final target = concept.translations[targetLanguageCode];
      if (source != null &&
          target != null &&
          _normalize(source) == normalized) {
        return TranslationResult(
          source: source,
          target: target,
          category: concept.category,
          visual: visualFor(concept.category, concept.source),
        );
      }
    }
    return null;
  }

  static List<TranslationResult> translationSuggestions({
    required String sourceLanguageCode,
    required String targetLanguageCode,
    String query = '',
    int limit = 12,
  }) {
    if (!coreLanguageCodes.contains(sourceLanguageCode) ||
        !coreLanguageCodes.contains(targetLanguageCode)) {
      return const [];
    }
    final normalizedQuery = _normalize(query);
    return concepts
        .where((concept) {
          if (normalizedQuery.isEmpty) return true;
          return _normalize(
            concept.translations[sourceLanguageCode] ?? '',
          ).contains(normalizedQuery);
        })
        .take(limit)
        .map(
          (concept) => TranslationResult(
            source: concept.translations[sourceLanguageCode]!,
            target: concept.translations[targetLanguageCode]!,
            category: concept.category,
            visual: visualFor(concept.category, concept.source),
          ),
        )
        .toList(growable: false);
  }

  static String _normalize(String value) => value
      .toLowerCase()
      .replaceAll(RegExp(r'''[.,!?،؛:;"'“”‘’…()\[\]{}_-]+'''), ' ')
      .replaceAll(RegExp(r'\s+'), ' ')
      .trim();

  static String visualFor(String category, String source) {
    final lower = source.toLowerCase();
    if (lower.contains('train') ||
        lower.contains('ticket') ||
        lower.contains('platform')) {
      return '🚆';
    }
    if (lower.contains('water')) return '💧';
    if (lower.contains('food') ||
        lower.contains('menu') ||
        lower.contains('vegetarian')) {
      return '🍽️';
    }
    if (lower.contains('bill') ||
        lower.contains('pay') ||
        lower.contains('card')) {
      return '💳';
    }
    if (lower.contains('hotel') ||
        lower.contains('room') ||
        lower.contains('reservation')) {
      return '🏨';
    }
    if (lower.contains('pharmacy') ||
        lower.contains('pain') ||
        lower.contains('ambulance')) {
      return '🩺';
    }
    if (lower.contains('email') ||
        lower.contains('meeting') ||
        lower.contains('deadline')) {
      return '💼';
    }
    if (lower.contains('phone') || lower.contains('wi-fi')) return '📱';
    if (lower.contains('photo')) return '📷';
    return switch (category) {
      'Introductions' => '👋',
      'Travel' => '🧭',
      'Food' => '🍽️',
      'Hotel' => '🏨',
      'Shopping' => '🛍️',
      'Health' => '🩺',
      'Emergencies' => '🆘',
      'Work' => '💼',
      'Relationships' => '🤝',
      'Technology' => '📱',
      'Airport' => '🛫',
      'At the Doctor' => '👨‍⚕️',
      'Pharmacy' => '💊',
      'Banking' => '🏦',
      'Public Services' => '🏛️',
      'Education' => '🎓',
      'Home' => '🏠',
      'Weather' => '🌦️',
      'Transport' => '🚌',
      _ => '💬',
    };
  }
